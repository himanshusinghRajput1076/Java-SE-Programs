package basicooprogramming;

public class CalAvg {
    private double n,m,p;
        public double setAvg(double a,double b,double c ){
            n=a;
           m=b;
           p=c;
            return 0;
        }
    public double ShowAvg()
    {
        double sum=0;
        sum=n+m+p;
        sum=sum/3;
        return sum;
    }

}

